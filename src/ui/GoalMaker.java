    private void addTaskButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTaskButtonActionPerformed
        task.setName(taskTextField.getText());
        task.setGoal(goal);
        task.setStatus('o');
        task.setCreated(new java.sql.Timestamp(new java.util.Date().getTime()));
        td.add(task);
        goal.setTask(td);
        
        DefaultTableModel model = (DefaultTableModel)taskTable.getModel();
        Object[] row = new Object[1];
        for (int i=0; i<td.size(); i++){
            row[0] = td.get(i).getName();
            model.addRow(row);
        }
    }//GEN-LAST:event_addTaskButtonActionPerformed

    private void deleteTaskButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteTaskButtonActionPerformed
        if (taskTable.getSelectedRow() != -1) {
            td.remove(taskTable.getSelectedRow());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ah! You gotta select the one you want to remove first.", "Looks like you forgot something", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteTaskButtonActionPerformed

