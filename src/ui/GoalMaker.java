    private void datePanelContinueButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_datePanelContinueButtonActionPerformed

        if (goalDatePicker.getDate() != null) {
            oDate = goalDatePicker.getDate();
            goalDate = oDateFormat.format(oDate);
            goal.setDeadline(java.sql.Date.valueOf(LocalDate.parse(goalDate)));
            wizardContainer.removeAll();
            wizardContainer.add(taskPanel);
            wizardContainer.repaint();
            wizardContainer.revalidate();
            taskGoalLabel.setText(goalNameTextBox.getText().trim());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Sorry to break it to you like this but a goal without a date is a wish!", "Give your goal a date", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_datePanelContinueButtonActionPerformed

    private void taskPanelSetGoalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_taskPanelSetGoalButtonActionPerformed
        goal.setName(goalNameTextBox.getText());
        //Since goal description field is optional,
        //this checks to see if a description was entered by the user
        //if not it puts a placeholder into the database
        if (descriptionTextArea.getText().trim().isEmpty()) {
            goal.setDescription("This goal was not described...");
        } else {
            goal.setDescription(descriptionTextArea.getText().trim());
        }
        
        goal.setCreated(new java.sql.Timestamp(new java.util.Date().getTime()));
        goal.setStatus('o');
        goal.setTypeid('g');
        em.getTransaction().begin();
        em.persist(goal);
        em.getTransaction().commit();
        
        JOptionPane.showMessageDialog(this, "You did it! You made a goal! Let's try our best to keep it!");
        this.dispose();

    }//GEN-LAST:event_taskPanelSetGoalButtonActionPerformed

